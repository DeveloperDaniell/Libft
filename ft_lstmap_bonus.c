#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*node;
	void	*result;

	if (!lst || !f)
		return (NULL);
	while (lst)
	{
		result = f(lst->content);
		if (!result)
		{
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		node = ft_lstnew(result);
		if (!node)
		{
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, node);
		lst = lst->next;
	}
	return (new_list);
}
